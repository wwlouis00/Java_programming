class Rank {
   public static void main(String[] args) {
      int num = 1000;
      System.out.print(num + "O");

      if(0 <= num && num <= 9)
         System.out.println("1旒啤C");
      else if(10 <= num && num <= 99)
         System.out.println("2旒啤C");
      else if(100 <= num && num <= 999)
         System.out.println("3旒啤C");
      else
         System.out.println("4旒匹HWC");
   }
}